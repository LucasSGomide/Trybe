// Declarar variáveis

let numA = 4;
let numB = 7;


// Adição Function

function soma(numA, numB) {
  return(numA + numB)
}

console.log(soma(numA, numB))

// Subtração
function subtracao(numA, numB) {
  return(numA - numB)
}

console.log(subtracao(numA, numB))

// Multiplicação
function multiplicacao(numA, numB) {
  return(numA * numB)
}

console.log(multiplicacao(numA, numB))

// Divisão
function divisao(numA, numB) {
  return(numA / numB)
}

console.log(divisao(numA, numB))

// Módulo
function modulo(numA, numB) {
  return(numA % numB)
}

console.log(modulo(numA, numB))
